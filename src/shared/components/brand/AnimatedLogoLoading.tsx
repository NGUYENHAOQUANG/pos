// ─── Component ──────────────────────────────────────────────────────

interface AnimatedLogoLoadingProps {
    /** Logo display size in pixels (default: 56) */
    size?: number;
}

export const AnimatedLogoLoading: React.FC<AnimatedLogoLoadingProps> = ({ size = 56 }) => {
    const arcRotation = useSharedValue(0);
    const pulse = useSharedValue(1);

    useEffect(() => {
        // Continuous arc rotation (clockwise)
        arcRotation.value = withRepeat(
            withTiming(360, {
                duration: 2000,
                easing: Easing.linear,
            }),
            -1
        );

        // Breathing pulse effect on entire logo
        pulse.value = withRepeat(
            withSequence(
                withTiming(1.08, { duration: 1200, easing: Easing.inOut(Easing.ease) }),
                withTiming(0.95, { duration: 1200, easing: Easing.inOut(Easing.ease) })
            ),
            -1,
            true
        );

        return () => {
            cancelAnimation(arcRotation);
            cancelAnimation(pulse);
        };
    }, [arcRotation, pulse]);

    // Animate only arcs rotation (around SVG center ~24.5, 24.5)
    const arcContainerStyle = useAnimatedStyle(() => ({
        transform: [{ rotate: `${arcRotation.value}deg` }],
    }));

    // Breathing pulse on the whole logo
    const pulseStyle = useAnimatedStyle(() => ({
        transform: [{ scale: pulse.value }],
    }));

    return (
        <Animated.View style={[styles.container, { width: size, height: size }, pulseStyle]}>
            {/* Rotating arcs layer (bottom — behind text) */}
            <Animated.View style={[StyleSheet.absoluteFill, arcContainerStyle]}>
                <Svg width={size} height={size} viewBox="-8 -8 65 65" fill="none">
                    <Defs>
                        <LinearGradient
                            id="loadingArcGradient"
                            x1="39.2763"
                            y1="13.8243"
                            x2="21.6258"
                            y2="36.4372"
                            gradientUnits="userSpaceOnUse"
                        >
                            <Stop stopColor="#FD6900" />
                            <Stop offset="0.5" stopColor="#FF647E" />
                            <Stop offset="1" stopColor="#006AFF" />
                        </LinearGradient>
                    </Defs>
                    <Path d={ARC_INNER} fill="url(#loadingArcGradient)" />
                    <Path d={ARC_MIDDLE} fill="url(#loadingArcGradient)" />
                    <Path d={ARC_OUTER} fill="url(#loadingArcGradient)" />
                </Svg>
            </Animated.View>

            {/* Static text layer (top — covers arcs behind text) */}
            <View style={StyleSheet.absoluteFill}>
                <Svg width={size} height={size} viewBox="-8 -8 65 65" fill="none">
                    {/* Stepped white mask: wider for MEBI, narrower for ECO — inner arc visible on right */}
                    <Path d="M-2 -2 H37 V16 H31 V32 H-2 Z" fill="white" />
                    {/* MEBI text (orange) */}
                    <G>
                        {MEBI_PATHS.map((d, i) => (
                            <Path key={`mebi-${i}`} d={d} fill="#FD6900" />
                        ))}
                    </G>
                    {/* ECO text (blue) */}
                    <G>
                        {ECO_PATHS.map((d, i) => (
                            <Path key={`eco-${i}`} d={d} fill="#006AFF" />
                        ))}
                    </G>
                </Svg>
            </View>
        </Animated.View>
    );
};

const styles = StyleSheet.create({
    container: {
        alignItems: 'center',
        justifyContent: 'center',
    },
});
